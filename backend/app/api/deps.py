# Authorisation
# ---------------------------------------------------------------------------


def require_plan(minimum_plan: str) -> Any:
    """Dependency factory — enforce a minimum subscription plan.

    Usage::

        @router.get("/export")
        async def export(org: Organization = Depends(require_plan("growth"))):
            ...
    """

    async def check_plan(
        org: Organization = Depends(get_current_org),
    ) -> Organization:
        user_level = PLAN_HIERARCHY.get(org.plan.value, 0)
        required_level = PLAN_HIERARCHY.get(minimum_plan, 0)
        if user_level < required_level:
            raise AuthorizationError.plan_upgrade_required(
                required_plan=minimum_plan,
                current_plan=org.plan.value,
            )
        return org

    return check_plan


async def require_admin(
    current_user: User = Depends(get_current_user),
) -> User:
    """Raise 403 unless the current user has account_type == admin."""
    if current_user.account_type != AccountType.admin:
        raise AuthorizationError.insufficient_permissions("admin")
    return current_user


# ---------------------------------------------------------------------------
# Request metadata helpers
